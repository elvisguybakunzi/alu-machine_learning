#!/usr/bin/env python3

"""
THis Script concatenates two arrays.

"""


def cat_arrays(arr1, arr2):
    """
    Concatenates two arrays.

    Parameters:
    arr1 (list of ints/floats): The first array.
    arr2 (list of ints/floats): The second array.

    Returns:
    list of ints/floats: A new list containing the
    elements of arr1 followed by the elements of arr2.

    """
    return arr1 + arr2
